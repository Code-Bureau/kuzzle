'use strict';

const { randomBytes } = require('crypto');

function randomNumber (max) {
  const bytes = Math.ceil(Math.log2(max) / 4);

  let number;

  do {
    number = randomBytes(bytes).readUIntBE(0, bytes);
  } while (number >= max);

  return number;
}

module.exports = function generateRandomName () {
  const adjective = adjectives[randomNumber(adjectives.length)];
  const name = names[randomNumber(names.length)];
  const number = randomNumber(65536);

  return `${adjective}-${name}-${number}`;
};

